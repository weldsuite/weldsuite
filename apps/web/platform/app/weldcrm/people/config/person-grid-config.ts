export const personGridConfig: EntityGridConfig<Person> = {
  entityName: 'Person',
  entityNamePlural: 'People',
  columns: personColumns,
  getEntityId: (p) => p.id,
  getEntityName: (p) => p.displayName,
  getEntityInitials: (p) => {
    const parts = p.displayName.split(/\s+/).filter(Boolean);
    if (parts.length >= 2) return (parts[0]!.charAt(0) + parts[parts.length - 1]!.charAt(0)).toUpperCase();
    return p.displayName.charAt(0).toUpperCase();
  },
  getEntityAvatar: (p) => getPersonAvatar(p),
  getEntitySubtitle: (p) => p.title ?? undefined,
  statusField: 'status',
  statusConfig: personStatusConfig,
  allowCustomColumns: true,
  enableCalculations: true,
  enableInlineEditing: true,
  enableRowSelection: true,
  enableExport: true,
  enableImport: false,
};
