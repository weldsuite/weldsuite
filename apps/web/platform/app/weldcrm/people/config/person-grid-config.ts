function getPersonAvatar(person: Person): string | undefined {
  if (person.avatarUrl) return person.avatarUrl;
  // Gravatar fallback when there's an email
  if (person.email) {
    return `https://www.gravatar.com/avatar/${encodeURIComponent(person.email.toLowerCase())}?d=mp&s=64`;
  }
  return undefined;
}

